#El **Simulador de Gasto Diario** es una aplicación de consola diseñada para ayudar a los usuarios
# a registrar y monitorear sus gastos diarios en diferentes categorías, como 
# comida, transporte, entretenimiento, entre otros. 
# Este simulador permite llevar un control básico de los gastos 
# diarios, semanales o mensuales y 
# obtener un resumen o reporte de los gastos en cada categoría. 
# Toda la información se guarda en un archivo JSON, 
# lo que permite mantener un historial de gastos entre distintas sesiones del programa.